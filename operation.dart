// operation

void main() {
  int num1 = 10;
  int num2 = 20;

  //addition
  int sum = num1 + num2;
  print(sum);
  // subtraction
  int sub = num1 - num2;
  print(sub);
  // mutiplication
  int mul = num1 * num2;
  print(mul);

  // division
  int div = num1 ~/ num2;
  print(div);
  // modulus
  int mod = num1 % num2;
  print(mod);

  // increament
  int y = num1++;
  int x = ++num2;
  print(y);
  print(x);
}
