// switch statement
void main() {
  int day = 2;

  switch (day) {
    case 1:
      print("Monday");
      break;
    case 2:
      print("Tuesday");
      break;
    case 3:
      print("Wednesday");
      break;
    case 4:
      print("Thurday");
      break;
    case 5:
      print("Friday");
      break;
    case 6:
      print("Saturday");
  }
}
